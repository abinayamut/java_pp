public class linear_tarversal_foreachloop {

	public static void main(String[] args) {
		int[] arr = {10, 20, 30, 40, 50};

        System.out.print("Traversal using foreach loop: ");
        for(int value : arr) {
            System.out.print(value + " ");
        }
        System.out.println();
	}}
